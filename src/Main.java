import java.awt.*;

public class Main {
    public static void main(String[] args) {
        //Escape sequence

        // '\' can be used to escape a special character succeeding it
        System.out.println("Hello \"Gokul\"");
        // To escape a backslash itself we can use another preceding it
        System.out.println("C:\\Documents\\CoreJava.txt");

        //Other escape sequences commonly used are \n and \t
        System.out.println("\n\nHi");
        System.out.println("\t\tHi");
    }
    }